        <h1>Home Page Design </h1>
        <p>Making a great first impression is really  important and we at EdgyPrints wanted to come up with a home page that appeals  to our niche target audience (Millennials). Upon  researching our main competitors (canvaschamps and wallartprints) &amp; other  successful e-commerce websites and our target customers thoroughly, we knew our  out homepage had to: </p>
        <ul>
          <li>To  attract and captivate visitors</li>
          <li>To  educate visitors on your brand</li>
          <li>To  encourage visitors to visit other pages on your website</li>
        </ul>
        <p>In order to achieve the  above, we decided to design our homepage in way to introduce&nbsp;and give  visitors a clear sense of your brand while keeping it simple with plenty of  whitespace, easy to read text and clearly labeled sections. Our homepage also  provides visitors with clear paths to other pages on the site and the font and  color were chosen to have a psychological effect  on our visitors and greatly influence how they feel  about your brand and how they interpret and engage with our brand.</p>
        <h1>Template Design </h1>
        <p>Instead of selecting and adapting a generic  template, we decided to create our own with fonts, color patterns and other  elements that are most applying to Millennial&rsquo;s. As such our research was  concentrated on analyzing e-commerce and consumer media sites that high  millennial active user&rsquo;s such as Amazon, shopify, ASOS, YouTube, etc.</p>
        <p>We found on analysis that: </p>
        <ul>
          <li>Millennial&rsquo;s mostly prefer  easily navigable and simple designs.</li>
          <li>Put emphasis on brand appeal </li>
          <li>Want a responsive design</li>
          <li>Millennial&rsquo;s want an engaging website</li>
        </ul>
        <p>We designed our template focusing on the  point&rsquo;s listed above.</p>
        <h1>Website Design </h1>
        <p>We based our website design upon reviewing  our competitor&rsquo;s website, target customers taste and tips shared by expert UX  designers (from Google, Shopify, ASOS etc.) online. Our research indicated that  5 mistakes that could potentially be killing our sales were: </p>
        <ul>
          <li>Lack of a Clear  Value Proposition</li>
          <li>Misguided Product Descriptions </li>
          <li>Failing to Properly Utilize Quality Images </li>
          <li>No Visual Hierarchy </li>
          <li>The Site Doesn't Look Trustworthy </li>
        </ul>
        <p>In order to avoid these mistakes and other  pitfalls, we asked our close friends and family members to fill out and rate  each iteration website using SUS (System Usability Scale) forms. Over time and  with critical feedback, we were able to rectify our mistakes. </p>
        <h1>How to find copyright-free graphics for websites</h1>
        <p>Using Google&rsquo;s advanced image search  options and websites such as freerange, unsplash, pexels, life of pix and host  of other stock graphic websites, looking for copyright free graphics to use and  share (even commercially) was not a problem. </p>
        <p>Using these resources, we have made sure to  include graphics that are copyright free.</p>
        <h1>How to optimize images for fast loading</h1>
        <p>As an e-commerce website, having fast  loading images was as important has having attractive ones. We decide to opt  for using JPEG format mostly for our images since other formats were naturally  larger. Additionally we opted for white backgrounds, eliminated unnecessary  product shots and load thumbnails first. </p>
        <p>As our store grows and we have a larger  number of products, we have decided to use Photoshop (save for web feature) or  other available photo compression software&rsquo;s that are able to compress image  files to 90% of their initial size without losing much quality. Additionally,  use of a Content Delivery Network (such as Amazon CloudFront or CloudFlare) to  host large media files has been discussed by our team.</p>
        <h1>Search Engine Optimization</h1>
        <p>We realize as a start-up if we are missing  out on clicks, we are probably missing out on potential sales. Having a  competitive ranking is a must. Every startup e-commerce website should go into  market with a strong SEO strategy in place to drive up traffic. </p>
        <p>For the purpose, we started with keyword  and competitor research using Google&rsquo;s free AdWork Keyword tool and Moz&rsquo;s  keyword tool. We choose to use keywords that were not too broad or too  competitive and the one&rsquo;s were most appealing to target audience i.e.  Generation Y focusing on relevancy, search volume and difficulty ranking. On  the competitor&rsquo;s front, we were able to find keywords used by them and also  there Domain Authority (DA) and Page Authority (PA) using the free Moz  toolbar.  Given, our established  competitors had higher DA and PA, we decided on using different keywords to  them as they competing with them would be very difficult and expensive at this  stage of our business.</p>
        <h1>Essential pages to be included in e-business sites </h1>
